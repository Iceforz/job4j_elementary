package ru.job4j.array;

public class ArrayDefinition {
    public static void main(String[] args) {
        String[] names = new String[4];
        names[0] = "Ilya";
        names[1] = "Kate";
        names[2] = "Vasya";
        names[3] = "Nik";
        System.out.println(names[0] + "\n" + names[1] + "\n" + names[2] + "\n" + names[3]);
    }
}
